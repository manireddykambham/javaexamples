package arthmetics;

public class PositiveOrNegative {
    public static void PositiveNegative(int numb) {
        if (numb < 0) {
            System.out.println("Negative number");
        } else if (numb>0) {
            System.out.println("Positive number");
        }else {
            System.out.println("Number is zero");
        }
    }
}
